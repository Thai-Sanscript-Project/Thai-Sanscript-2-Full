ข้อตกลงการปริวรรต อักษรไทย-คงรูป (แบบแผน) เป็นอักษรอื่นๆ	Agreement of using Thai character (Traditional, formal, official) to other characters.
1.พยัญชนะสังโยค: การพิมพ์อักษรไทยในลักษณะพยัญชนะสังโยคที่ผสมกับ สระหน้าพยัญชนะ เช่น รูปสระเอ, สระไอ สระโอ และ สระเอา เมื่อพิมพ์ตัวอักษรไทย จะพิมพ์สระหน้าไว้พยัญชนะสังโยคตัวสุดท้าย 	1. Conjunction Consonant: The Thai-language typing uses the form of mixed consonants. Vowels are placed before consonants such as “เ=a”, “ไ=i”, “โ=o”, and “เ า-ao”. When typing Thai, the prefix vowels will be positioned in the last conjunction consonant. 
แต่หาก พยัญชนะสังโยคควบกับ 'ร' สามารถวางไว้พยัญชนะสังโยคแรกได้ เช่น draupatī สามารถพิมพ์ได้เป็น เทฺราปตี ทฺเราปตี , kṣetre สามารถพิมพ์เป็น กฺเษตฺเร กฺเษเตฺร ยังไม่รองรับการพิมพ์ เกฺษเตฺร	However, if the conjunction consonant is with 'r', then the vowel can be placed in the first conjunction consonant. For example, draupatī can be printed as a “เทฺราปตี”, “ทฺเราปตี”; kṣetre can be printed as a “กฺเษตฺเร”, “กฺเษเตฺร”. At this time, it does not support in a form of “เกฺษเตฺร” yet.
2.พยัญชนะที่ผสมกับสระ ฤ ฤๅ ฦ และ ฦๅ: จะไม่ใส่พินทุไว้ใต้พยัญชนะตัวนั้น เช่น mṛga สามารถพิมพ์เป็น มฤค	2. Consonants mixed with vowel “ฤ- rue“ “ฤๅ- rue“ “ฦ- lue“ and “ฦๅ- lue“: do not put a dot under the consonant such as mṛga can be printed as a “มฤค”.
3.รูปเครื่องหมายวิสรรคะ: ใช้เป็น วิสรรชนีย์ (ะ)	3. The sign “: Colon (punctuation)”
Use as a “วิสรรชนีย์ (wí-sǎn-chá-nii)” (ะ - a)).
4.รูปเครื่องหมายจันทรพินทุหรืออนุนาสิกะ: ถอดเป็นไม้หันอากาศและมีนิคหิตข้างบน (อัํ) เช่น tālam̐ เป็น ตาลัํ	4. The sign of Candarapindu (จันทรพินทุ) or Anunāsika (อนุนาสิกะ):
It is a Mai Han-Akat (◌ั) and has the above dot (อัํ).
For example, “tālam̐” is printed as “ตาลัํ”.
	
