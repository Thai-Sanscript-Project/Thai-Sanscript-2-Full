ศูรางคมธารณีสูตร
@
ศูรางคมธารณีสูตร เป็นธารณีที่อยู่ในพระสูตรมหายานชื่อ ศูรางคมสูตร ผูกที่ ๓ ในส่วนธารณีแบ่งเป็น ๕ บท และเป็นธารณีมนต์ที่น่าศึกษาสำหรับผู้สนใจพุทธศาสนาในนานานิกายและผู้สนใจในภาษาสันสกฤต เพราะศูรางคมธารณีสูตรเป็นธารณีมนต์ที่สำคัญบทหนึ่งของฝ่ายมนตรยานและฝ่ายมหายานในประเทศจีน  ในพระสูตรยกย่องว่าเป็นพระคาถาที่ทรงบุญญานุภาพมากหลาย ในศูรางคมธารณีสูตรมีเนื้อหากล่าวถึงพระอานนท์เถระถูกนางมาตังคีทำร้ายด้วยมนต์ดำ พระพุทธองค์ทรงอยู่ในสมาธิจึงทรงเปล่งพุทธรัศมีจากพระอุษณีย์(คือ ส่วนนูนกลางพระเศียร) แสดงพระธารณีบทนี้เพื่อช่วยพระอานนท์ จากนั้นจึงทรงตรัสแสดงธารณีนี้แก่สรรพสัตว์

ในฝ่ายมนตรยานนับถือว่าเป็นธารณีประจำพระองค์ของพระสิตาตปัตราโพธิสัตว์ คือ พระโพธิสัตว์ผู้ทรงสิตปัตร คือ มีพันหัตถ์ ทั้งยังนับถือกันว่าจะทรงปกป้องคุ้มครองสรรพสัตว์ทั้งหลายและเหล่าผู้บูชา ประดุจฉัตรสีขาวบริสุทธิ์ ที่กางกั้นบนพระเศียรของปวงพระพุทธเจ้า
