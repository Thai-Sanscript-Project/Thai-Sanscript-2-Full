११३ ज्वर एकाहिका द्वैतीयका स्त्रैतीयका चतुर्-थका॥
११४ नित्य-ज्वरा विषम-ज्वरा वतिका पैत्तिका।
श्लेष्मिका सन्-निपतिका सर्व-ज्वरा॥
११५ शिरोर्त्ति अर्धवभेदक अरोचक।
११६ अक्षि-रोगं नस-रोगं मुख-रोगं हृद्-रोगं गल-ग्रहं।
११७ कर्न्ण-शूलं दन्त-शूलं हृदय-शूलं मर्म-शूलं।
११८ पार्श्व-शूलं पृष्ठ-शूलं उदर-शूलं कण्टी-शूलं।
११९ वस्ति-शूलं ऊरु-शूलं जाङ्घ-शूलं हस्त-शूलं।
पाद-शूलं सर्वाङ्ग-प्रत्यङ्ग-शूलं॥
१२० भूत वेताड डाक-डाकिनी ज्वर॥
१२१ दद्रु काण्डु किटिभलोतवै सर्प-लोहालिङ्ग।
शूषत्र सगर विश-योग।
१२२ अग्नि उदक मर वैर कान्तार अकालं-मृत्यु॥
१२३ त्रैबुक त्रै-लटक वृश्चिक सर्प नकुल।
सिंघ व्याघ्र ऋक्ष तरक्ष मृग।
स्व-पर जीव तेषां सर्वेषां॥
१२४ षितातपत्रं महा-व्ज्रोष्णीषं महा-प्रत्यङ्गिरं॥
१२५ यावद्वा-दश योजनाभ्यन्तरेण।
सीमा-बन्धं करोमि।
दिशा-बन्धं करोमि।
पार-विद्या-बन्धं करोमि।
तेजो-बन्धं करोमि।
हस्त-बन्धं करोमि ।
पाद-बन्धं करोमि।
सर्वाङ्ग-प्रत्यङ्ग-बन्धं करोमि॥
१२६ तद्यथा: ॐ अनले अनले विशदे विशदे वीर व्ज्र-धरे।
बन्ध बन्धनि।
वज्र-पाणि फट्! हूं त्रूं फट्! स्वाहा॥
नमः स्तथागताय सुगताय अर्हते सम्यक्-संबुद्धाय।
सिद्ध्यन्तु मन्त्र-पद स्वाहा॥